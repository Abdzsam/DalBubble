function process(data) {
    const result = data
        .split("Course\tSection\tDate\tStart Time\n")
        .map((line) => line.slice(0, line.length - 5))
        .join("")
        .split("\n")
        .map((line) => line.split("\t"))
        .reduce((curr, acc) => {
            const [courseNotTrimmed, section, date, time] = acc;
            const course = courseNotTrimmed.slice(
                0,
                courseNotTrimmed.length - 2
            );
            if (curr[course]) {
                curr[course].push({ section, date, time });
            } else {
                curr[course] = [{ section, date, time }];
            }
            return curr;
        }, {});

    console.log(result);
    fs.writeFileSync("exam_schedule.json", JSON.stringify(result));
}

